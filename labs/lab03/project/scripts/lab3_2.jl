using DrWatson
@quickactivate "project"
using Plots
using DifferentialEquations

# Начальные условия
x0 = 44000  # численность первой армии
y0 = 33000   # численность второй армии
t0 = 0.0   # начальный момент времени

# Параметры модели
a = 0.43  # константа, характеризующая степень влияния различных факторов на потери
b = 0.79  # эффективность боевых действий армии y
c = 0.79  # эффективность боевых действий армии x
h = 0.23  # константа, характеризующая степень влияния различных факторов на потери

tmax = 1.0  # предельный момент времени
dt = 0.05  # шаг изменения времени

# Функция подкрепления для армии x
function P(t)
    return sin(2*t) + 1
end

# Функция подкрепления для армии y
function Q(t)
    return cos(2*t)
end

# Система дифференциальных уравнений
function syst(dy, y, p, t)
    dy[1] = -a * y[1] - b * y[2] + P(t)  # изменение численности первой армии
    dy[2] = -c * y[1] * y[2] - h * y[2] + Q(t)  # изменение численности второй армии
end

# Вектор начальных условий
u0 = [x0; y0]

# Временной интервал
tspan = (t0, tmax)

# Создаём массив временных точек для сохранения решения
t_save = t0:dt:tmax

# Создаём задачу Коши для системы ОДУ
prob = ODEProblem(syst, u0, tspan)

# Решаем систему дифференциальных уравнений
sol = solve(prob, saveat=t_save)

# Построение графиков решений
plot(sol,
    label=["Численность армии X" "Численность армии Y"],
    title="Модель боевых действий № 1",
    xlabel="Время",
    ylabel="Численность армии",
    linewidth=2,
    legend=:topright,
    grid=true)


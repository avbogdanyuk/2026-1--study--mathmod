#= 
# Моделирование системы "хищник-жертва" (модель Лотки-Вольтерры)
#
# Данный скрипт реализует модель Лотки-Вольтерры для описания динамики
# взаимодействия двух популяций: хищников и жертв.
# Система описывается дифференциальными уравнениями:
# dx/dt = -c*x + d*x*y  (изменение численности хищников)
# dy/dt = a*y - b*x*y    (изменение численности жертв)
# где x - численность хищников, y - численность жертв
=#

using Plots
using DifferentialEquations

# Начальные условия
const x0 = 4    # начальная численность хищников
const y0 = 14   # начальная численность жертв

# Параметры модели
const c = 0.36  # коэффициент увеличения числа хищников
const d = 0.035 # коэффициент смертности жертв
const a = 0.38  # коэффициент естественной смертности хищников
const b = 0.037 # коэффициент естественного прироста жертв

# Временной интервал моделирования
T = (0, 400)

# Вектор начальных условий
u0 = [x0, y0]

# Кортеж параметров
p = (a, b, c, d)

"""
    F(du, u, p, t)

Функция, описывающая правые части дифференциальных уравнений модели.

Аргументы:
- du: вектор производных (выходной параметр)
- u: вектор текущих значений (x - хищники, y - жертвы)
- p: кортеж параметров (a, b, c, d)
- t: время
"""
function F(du, u, p, t)
    a, b, c, d = p
    du[1] = -c*u[1] + d*u[1]*u[2]   # изменение численности хищников
    du[2] = a*u[2] - b*u[1]*u[2]    # изменение численности жертв
end

# Создание и решение задачи ОДУ
prob1 = ODEProblem(F, u0, T, p)
sol1 = solve(prob1, dtmax=0.1)

# Фазовый портрет: зависимость численности хищников от численности жертв
plt = plot(
    sol1,
    vars=(2,1),
    color=:red,
    label="Фазовый портрет",
    title="Зависимость численности хищников от численности жертв",
    xlabel="Численность жертв",
    ylabel="Численность хищников"
)

# Графики изменения численности хищников и жертв во времени
plt2 = plot(
    sol1,
    vars=(0,1),
    color=:blue,
    label="Численность хищников",
    title="Изменение численности хищников и жертв",
    xlabel="t"
)
plot!(
    plt2,
    sol1,
    vars=(0,2),
    color=:green,
    label="Численность жертв"
)

# Сохранение графиков
savefig(plt, "lab05_1.png")   # график зависимости числа хищников от числа жертв
savefig(plt2, "lab05_2.png")  # графики изменения числа хищников и жертв
